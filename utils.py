"""Хелперы для склонения мимриков."""

def mimriks(n: int) -> str:
    """Возвращает правильно склонённое слово 'мимрик' для числа n."""
    if n < 0:
        n = -n
    if n % 100 in (11, 12, 13, 14):
        return "мимриков"
    if n % 10 == 1:
        return "мимрик"
    if n % 10 in (2, 3, 4):
        return "мимрика"
    return "мимриков"
